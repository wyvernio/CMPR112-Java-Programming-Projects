
public class Percentages {
  
  public static void main( String [] args ) {
  
    double variableOne = 2.0;
    double variableTwo = 5.0;
    double computePercentage;
    double computePerception;
    
    computePerception = variableOne / variableTwo;
    computePercentage = computePerception * 100;
    
    System.out.println(variableOne + " Is " + computePercentage + "% of " + variableTwo);
    
  }
}





